# Задача 30:  Заполните массив элементами арифметической прогрессии.
# Её первый элемент, разность и количество элементов нужно ввести с клавиатуры.
# Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
# Каждое число вводится с новой строки.

def generate_arithmetic_progression():
    # Ввод первого элемента, разности и количества элементов
    a1 = int(input("Введите первый элемент арифметической прогрессии: "))
    d = int(input("Введите разность арифметической прогрессии: "))
    n = int(input("Введите количество элементов: "))

    # Создание и вывод массива с элементами арифметической прогрессии
    progression = [a1 + (i - 1) * d for i in range(1, n + 1)]

    # Вывод массива
    print("Арифметическая прогрессия:")
    for element in progression:
        print(element)

# Вызов функции для генерации арифметической прогрессии
generate_arithmetic_progression()

